#include "libft.h"

char	*ft_strdup(const char *str)
{
	char *new;
	int	i;
	int	size;

	size = ft_strlen(str);
	if (!(new = malloc(sizeof(char) * (size + 1))))
		return (NULL);
	while (str[i] != '\0')
	{
		new[i] = str[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
